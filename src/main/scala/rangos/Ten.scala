package rangos


/**
 * La clase `Ten` representa el rango de la carta dos en un juego de cartas.
 *
 * Esta clase hereda de `Rangos` y define el valor y el orden del rango de la carta dos.
 */
class Ten extends Rangos{
  /**
   * El valor de la carta "Diez"
   */
  val value: Int = 10
  /**
   * El orden de la carta "Diez", que define su posición en el rango.
   * Este valor se utiliza en operaciones de comparación y ordenación de cartas.
   */
  val order:Int = 10


}
