package rangos

/**
 * La clase `Three` representa el rango de la carta dos en un juego de cartas.
 *
 * Esta clase hereda de `Rangos` y define el valor y el orden del rango de la carta dos.
 */
class Three extends Rangos{
  /**
   * El valor de la carta "Tres"
   */
  val value: Int = 3
  /**
   * El orden de la carta "Tres", que define su posición en el rango.
   * Este valor se utiliza en operaciones de comparación y ordenación de cartas.
   */
  val order = 3


}
